

CREATE TABLE dm_corep_recap_measure (
    official_approach_indicator            NUMBER(1, 0),
    reporting_date                         DATE,
    system_id                              VARCHAR2(3 CHAR)
)
  PARTITION BY LIST (REPORTING_DATE,OFFICIAL_APPROACH_INDICATOR) 
  SUBPARTITION BY LIST (SYSTEM_ID)
  SUBPARTITION TEMPLATE
  ( SUBPARTITION P_AML VALUES ('AML'),
    SUBPARTITION P_BMG VALUES ('BMG'),
    SUBPARTITION P_WUH VALUES ('WUH'),
    SUBPARTITION P_WUN VALUES ('WUN'),
    SUBPARTITION P_DEFAULT VALUES (DEFAULT) 
  )
  (
    --Patritions for 2016
    PARTITION P_201601_0 VALUES (TO_DATE('2016-01-31 00:00:00', 'YYYY-MM-DD HH24:MI:SS'),0) COMPRESS FOR ARCHIVE HIGH,
    PARTITION P_201601_1 VALUES (TO_DATE('2016-01-31 00:00:00', 'YYYY-MM-DD HH24:MI:SS'),1) COMPRESS FOR ARCHIVE HIGH,
    PARTITION P_201612_0 VALUES (TO_DATE('2016-12-31 00:00:00', 'YYYY-MM-DD HH24:MI:SS'),0) COMPRESS FOR ARCHIVE HIGH,
    PARTITION P_201612_1 VALUES (TO_DATE('2016-12-31 00:00:00', 'YYYY-MM-DD HH24:MI:SS'),1) COMPRESS FOR ARCHIVE HIGH,
    PARTITION P_202512_1 VALUES (TO_DATE('2025-12-31 00:00:00', 'YYYY-MM-DD HH24:MI:SS'),1) COMPRESS FOR ARCHIVE HIGH
  )

ENABLE ROW MOVEMENT;

